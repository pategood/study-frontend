// 标签语义化是为了 让机器更好的理解 html信息


// HTML语义化的目的是什么？

// 语义化的网页的好处， 最主要的就是对搜索引擎友好， 有了良好的结构和语义你的网页内容自然容易被搜索引擎抓取

// 方便其他设备解析

// 语义化使得代码更具有可读性

// 提升用户体验:例如title、alt可以用于解释名称或者解释图片信息， 以及label标签的灵活运用

// 有利于SEO: 爬虫依赖标签来确定关键字的权重

